// Функция для получения вопросов по фильтрам
export function getQuestions(
  taskNumber?: number,
  source?: 'fipi' | 'doshinsky'
): Question[] {
  return QUESTIONS.filter(q => {
    if (taskNumber && q.taskNumber !== taskNumber) return false;
    if (source && q.source !== source) return false;
    return true;
  });
}

// Генерация варианта ОГЭ (по одному вопросу из каждого задания 1-13)
export function generateOGEVariant(source?: 'fipi' | 'doshinsky'): Question[] {
  const variant: Question[] = [];
  
  // Для заданий 1 и 13 создаем заглушки (они требуют загрузки файлов)
  const task1: Question = {
    id: 'q_1_oge_variant',
    taskNumber: 1,
    source: source || 'fipi',
    text: 'Задание 1: Написание изложения. Загрузите файл с текстом изложения.',
    correctAnswer: 'Загрузка файла',
    explanation: 'Задание требует ручной проверки преподавателем',
  };
  
  const task13: Question = {
    id: 'q_13_oge_variant',
    taskNumber: 13,
    source: source || 'fipi',
    text: 'Задание 13: Сочинение-рассуждение. Загрузите файл с текстом сочинения.',
    correctAnswer: 'Загрузка файла',
    explanation: 'Задание требует ручной проверки преподавателем',
  };
  
  variant.push(task1);
  
  // Для заданий 2-12 выбираем случайные вопросы
  for (let taskNum = 2; taskNum <= 12; taskNum++) {
    const taskQuestions = getQuestions(taskNum, source);
    if (taskQuestions.length > 0) {
      const randomQuestion = taskQuestions[Math.floor(Math.random() * taskQuestions.length)];
      variant.push(randomQuestion);
    }
  }
  
  variant.push(task13);
  
  return variant;
}